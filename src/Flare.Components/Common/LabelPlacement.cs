namespace Flare.Components;

/// <summary>Position of a control's label relative to the control (checkbox, radio, switch).</summary>
public enum LabelPlacement
{
    /// <summary>Label after the control (default, e.g. to the right in LTR).</summary>
    End,
    /// <summary>Label before the control (e.g. to the left in LTR).</summary>
    Start,
}
