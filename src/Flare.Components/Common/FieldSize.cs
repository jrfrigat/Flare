namespace Flare.Components;

/// <summary>Size of a form field control (input, select, multi-select). Medium is the default.</summary>
public enum FieldSize
{
    /// <summary>Extra small - dense, for toolbars and inline filters.</summary>
    Xs,
    /// <summary>Small - compact.</summary>
    Sm,
    /// <summary>Medium - default field size.</summary>
    Md,
    /// <summary>Large.</summary>
    Lg,
    /// <summary>Extra large.</summary>
    Xl,
}
