namespace Flare.Components;

/// <summary>Rotation applied to each tab's label in <see cref="FlareTabs"/>.</summary>
public enum TabLabelRotation
{
    /// <summary>No rotation (0 degrees). Default.</summary>
    None,
    /// <summary>Rotate the label 90 degrees clockwise.</summary>
    Rotate90,
    /// <summary>Rotate the label 180 degrees (upside down).</summary>
    Rotate180,
    /// <summary>Rotate the label 270 degrees clockwise (90 counter-clockwise).</summary>
    Rotate270,
}
