namespace Flare.Components;

/// <summary>Controls the visual layout of form fields inside a FlareForm.</summary>
public enum FormLayout
{
    /// <summary>Fields stack vertically (default).</summary>
    Vertical,
    /// <summary>Label and input sit side by side in each row.</summary>
    Horizontal,
    /// <summary>Fields flow inline, wrapping as needed.</summary>
    Inline,
}
