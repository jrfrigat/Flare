namespace Flare.Components;

/// <summary>Layout of the <c>FlareDateTimePicker</c> popup.</summary>
public enum DateTimeVariant
{
    /// <summary>Panes on wide viewports (>= md), tabs on narrow ones.</summary>
    Auto,
    /// <summary>Date and time on switchable tabs (compact).</summary>
    Tabs,
    /// <summary>Calendar and time shown side by side as two panes.</summary>
    Panels,
}
