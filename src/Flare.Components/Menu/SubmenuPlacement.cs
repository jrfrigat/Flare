namespace Flare.Components;

/// <summary>Placement of the submenu relative to the parent item.</summary>
public enum SubmenuPlacement
{
    /// <summary>Submenu opens to the right of the parent item.</summary>
    End,
    /// <summary>Submenu opens to the left of the parent item.</summary>
    Start,
    /// <summary>Submenu opens below the parent item.</summary>
    Bottom,
    /// <summary>Submenu opens above the parent item.</summary>
    Top,
}
