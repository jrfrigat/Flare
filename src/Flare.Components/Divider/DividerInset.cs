namespace Flare.Components;

/// <summary>
/// Inset style of a <see cref="FlareDivider"/>, per Material Design 3.
/// </summary>
public enum DividerInset
{
    /// <summary>Full-bleed divider spanning the full width (the default).</summary>
    None,

    /// <summary>Indented from the leading edge only.</summary>
    Inset,

    /// <summary>Indented from both edges.</summary>
    MiddleInset,
}
